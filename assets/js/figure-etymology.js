function addFigureEtymology(){
  if(!window.location.pathname.includes('/figures/')) return;
  const file=window.location.pathname.split('/').filter(Boolean).pop();
  const data=figureNameEtymology[file];
  if(!data || document.querySelector('.figure-etymology')) return;
  const main=document.querySelector('main');
  if(!main) return;
  const section=document.createElement('section');
  section.className='figure-etymology';
  const rows=data.parts.map(p=>`<tr><td><strong>${p[0]}</strong></td><td>${p[1]}</td><td>${p[2]}</td><td>${p[3]}</td><td>${p[4]}</td></tr>`).join('');
  section.innerHTML=`
    <div class="section-title">
      <h2 data-lang="he">${data.heTitle}</h2>
      <h2 data-lang="en">${data.enTitle}</h2>
      <p data-lang="he">${data.heIntro}</p>
      <p data-lang="en">${data.enIntro}</p>
    </div>
    <div class="panel table-wrap">
      <table>
        <thead><tr><th>רכיב</th><th>Transliteration</th><th>משמעות בעברית</th><th>Meaning in English</th><th>הסבר</th></tr></thead>
        <tbody>${rows}</tbody>
      </table>
    </div>`;
  const firstResearch=document.querySelector('.section-title');
  if(firstResearch && firstResearch.parentElement) firstResearch.parentElement.insertAdjacentElement('beforebegin',section);
  else main.appendChild(section);
}

document.addEventListener('DOMContentLoaded',addFigureEtymology);
